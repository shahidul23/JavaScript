// Arithmetic Operator -> + , -, *, /, %(Modules) ** (Exponent)

var a = 4;
var b = 5;

var sum = a + b;

document.write(sum,"<br/>");

var sub = a - b;
document.write(sub,"<br/>");

var mul = a * b;
document.write(mul,"<br/>");

var mod = a % b;
document.write(mod,"<br/>");

var exp = a ** b;
document.write(exp,"<br/>");
